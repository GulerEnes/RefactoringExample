package ybu.datastruct;

public class Item {
    public String name;
    public String type;
    public String unit;
    public double amount;
    public double calories;

    public Item(String name, String type, String unit, double amount, double calories) {
        this.name = name;
        this.type = type;
        this.unit = unit;
        this.amount = amount;
        this.calories = calories;
    }


}
